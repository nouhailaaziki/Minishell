#include "../../launchpad.h"

void	free_env_list(t_env **env)
{
	t_env	*tmp;

	while (*env)
	{
		tmp = (*env)->next;
		free((*env)->key);
		free((*env)->value);
		free((*env));
		*env = tmp;
	}
}
